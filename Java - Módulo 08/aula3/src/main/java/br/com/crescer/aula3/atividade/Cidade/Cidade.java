package br.com.crescer.aula3.atividade.Cidade;

/**
 *
 * @author joao.silva
 */
public class Cidade {
    private long id;
    private String nome;
    private long estado;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public long getEstado() {
        return estado;
    }

    public void setEstado(long estado) {
        this.estado = estado;
    }
    
}
